 ## Swift 5 ABI Stable
 
    Mit Swift 5 wurde die ABI auf Stable gesetzt.
    ABI Stability ist ein Meilenstein für jede Programmiersprache da Sie dann Stabil genug ist um in ein Betriebssystem eingebettet zu werden.
    Das heißt man kann eine mit Swif5 erzeugte Libary unter Swift 5.1, Swift 6 und weitere einbinden ohne das Binary neu erzeugen zu müssen.
 
    Ohne ABI Stability muss immer die richtige Version der Runntime mit geliefert werden.
